/** i18n 지역 별칭 로직 — SDK 가 \`ko-KR\` 을 영어로 떨구는 버그를 우리 쪽에서 막는다. */
export function tplI18nIndex() {
  return `/* AUTO-GENERATED by miniapp-panel-studio. panel-sdk 의 지역코드 버그 대응 포함. */
import { kit } from '@ray-js/panel-sdk';
import { getSystemInfoSync } from '@ray-js/ray';
import strings from './strings';

const { I18N } = kit;

const REGIONS: Record<string, string[]> = {
  ko: ['ko-KR', 'ko_KR', 'ko-kr', 'ko_kr'],
  en: ['en-US', 'en_US', 'en-us', 'en_us', 'en-GB', 'en_GB'],
};

function withRegionAliases<T extends Record<string, unknown>>(table: T): T {
  const expanded: Record<string, unknown> = { ...table };
  Object.entries(REGIONS).forEach(([base, variants]) => {
    if (!table[base]) return;
    variants.forEach(v => { expanded[v] = table[base]; });
  });
  return expanded as T;
}

const Strings = new I18N(withRegionAliases(strings));

const systemLang = String(getSystemInfoSync()?.language ?? '');
const base = systemLang.toLowerCase().split(/[-_]/)[0];
if (base && base !== 'en' && (strings as Record<string, unknown>)[base]) {
  Strings.setLanguage(base);
}

export default Strings;
`;
}

/** 외부 URL 열기 — native 모듈 부재에 대비한 이중 폴백. haatz 실기기 검증본. */
export function tplOpenExternal() {
  return `/* AUTO-GENERATED by miniapp-panel-studio. native 모듈 부재 대비 이중 폴백. */
import { openURL, openUrlForceDefaultBrowser } from '@ray-js/ray';

/**
 * 외부 주소를 미니앱 밖에서 연다. openURL(MiniKit) → openUrlForceDefaultBrowser(BizKit)
 * 순으로 시도하고, 둘 다 없으면 onFail. 실패해도 던지지 않는다.
 */
export function openExternal(url: string, onFail: () => void): void {
  openURL({
    url,
    fail: () => openUrlForceDefaultBrowser({ url, fail: onFail }),
  });
}
`;
}

