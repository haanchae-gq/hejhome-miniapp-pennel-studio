/**
 * 고정 템플릿 — 실기기 대응 지식이 박혀 있어 모델과 무관하게 검증된 형태로 찍는다.
 *
 * 여기 담긴 것은 haatz 레포가 실기기에서 피 흘려 얻은 결정들이다:
 *  - `ignoreDpResponse` 를 빼는 이유 (모드 변경 후 5초 센서 리포트 유실)
 *  - `setDps` 배치 전송 (예약이 낡은 시각으로 걸리는 경쟁)
 *  - i18n 지역 별칭 (`ko-KR` → 영어로 떨어지는 SDK 버그)
 *  - openExternal 이중 폴백 (native 모듈 부재)
 *
 * 비개발자는 이 파일들을 건드리지 않는다. 디바이스 키(dk)와 DP 상태 타입만 주입한다.
 */

const dpStateType = panel => {
  const tsType = dp =>
    dp.type === 'bool' ? 'boolean' :
    dp.type === 'value' ? 'number' :
    dp.type === 'string' ? 'string' :
    dp.type === 'color' ? 'string' :  // colour_data raw HSV hex
    dp.type === 'enum' ? dp.range.map(v => `'${v}'`).join(' | ') :
    'unknown';
  const rows = panel.dps
    .filter(dp => dp.semantic !== 'unrendered')
    .map(dp => `  ${dp.camel}?: ${tsType(dp)};`);
  // 렌더 안 하는 DP 는 형만 남긴다 (haatz 의 faultRemind 주석과 같은 취지).
  const unrendered = panel.dps
    .filter(dp => dp.semantic === 'unrendered')
    .map(dp => `  /** DP ${dp.id} — 패널이 소비하지 않는다. 기기가 보내므로 형만 둔다. */\n  ${dp.camel}?: unknown;`);
  return [...rows, ...unrendered].join('\n');
};

export function tplUseDp(panel) {
  const dk = panel.meta.deviceKey;
  return `/* AUTO-GENERATED by miniapp-panel-studio. 디바이스 통신 seam — 실기기 대응 지식 포함. */
import { useDevInfo, useProps } from '@ray-js/panel-sdk';
import { SmartDeviceSchema, devices } from '@/devices';

/** 전 DP 선택. 미보고 DP 는 absent key(undefined). */
export type DpState = {
${dpStateType(panel)}
};

export function useDpState(): DpState {
  return useProps<SmartDeviceSchema, any>() as DpState;
}

/** \`undefined\` until the device model reports otherwise — do not read as offline. */
export function useOnline(): boolean {
  const isOnline = useDevInfo<SmartDeviceSchema>(info => info.isOnline);
  return isOnline !== false;
}

/** Fire-and-forget. offline tap 을 unhandled rejection 으로 띄우지 않는다. */
export function setDp<K extends keyof DpState>(code: K, value: NonNullable<DpState[K]>): void {
  devices.${dk}.publishDps({ [code]: value } as any).catch(() => undefined);
}

/** 여러 DP 를 한 프레임에 — 예약 토글이 낡은 시각으로 걸리는 경쟁을 없앤다. */
export function setDps(dps: Partial<DpState>): void {
  devices.${dk}.publishDps(dps as any).catch(() => undefined);
}
`;
}

export function tplDevicesIndex(panel) {
  const dk = panel.meta.deviceKey;
  return `/* AUTO-GENERATED by miniapp-panel-studio. */
import { SmartDeviceModel, SmartSupportAbility } from '@ray-js/panel-sdk';
import { createDpKit } from '@ray-js/panel-sdk/lib/sdm/interceptors/dp-kit';
import { defaultSchema } from '@/devices/schema';

export type SmartDeviceSchema = typeof defaultSchema;

export const dpKit = createDpKit<SmartDeviceSchema>({
  sendDpOption: {
    // 보낸 DP 를 로컬 상태에 즉시 반영. 낙관적 롤백을 쓰지 않으므로
    // \`ignoreDpDataResponse\` 는 일부러 뺐다 — 그것을 켜면 모든 앱 조작 뒤 5초간
    // 기기의 정당한 센서 리포트가 통째로 유실된다.
    immediate: true,
  },
});

export const devices = {
  ${dk}: new SmartDeviceModel<SmartDeviceSchema, { support: SmartSupportAbility<SmartDeviceSchema> }>({
    interceptors: dpKit.interceptors,
    abilities: [new SmartSupportAbility()],
  }),
};
`;
}

export function tplComposeLayout(panel) {
  const dk = panel.meta.deviceKey;
  return `/* AUTO-GENERATED by miniapp-panel-studio. */
import React, { Component } from 'react';
import { devices, dpKit } from '@/devices';

/** Device-init HOC — onLaunch 가 기기 모델을 먼저 세운다. Redux 없음. */
const composeLayout = (Comp: React.ComponentType<any>) => {
  return class PanelComponent extends Component<any> {
    async onLaunch() {
      devices.${dk}.init();
      devices.${dk}.onInitialized(() => {
        dpKit.init(devices.${dk});
      });
    }
    render() {
      return <Comp {...this.props} />;
    }
  };
};

export default composeLayout;
`;
}

